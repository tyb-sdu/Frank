"""
示例：水分子 HF/6-31G* 计算

使用 Frank 生成的代码，可直接运行。
"""

# ============================================================
#  导入模块
# ============================================================
from pyscf import gto, scf

# ============================================================
#  基组设置
# ============================================================
BASIS = "6-31g*"

# ============================================================
#  分子定义
# ============================================================
# 水 (H2O)
mol = gto.Mole()
mol.atom = '''
    O  0.000  0.000  0.117
    H  0.000  0.757 -0.469
    H  0.000 -0.757 -0.469
'''
mol.basis = BASIS
mol.charge = 0
mol.spin = 0  # 自旋多重度 = 1 (单重态)
mol.verbose = 4
mol.build()

# ============================================================
#  SCF 计算
# ============================================================
mf = scf.RHF(mol)
mf.kernel()

print(f"SCF 能量: {mf.e_tot:.10f} Hartree")

# ============================================================
#  分子轨道分析
# ============================================================
mo_energy = mf.mo_energy
mo_occ = mf.mo_occ
n_occ = int(sum(mo_occ) / 2)

print(f"\n分子轨道分析:")
print(f"  占据轨道数: {n_occ}")
print(f"  HOMO 能量: {mo_energy[n_occ-1]*27.2114:.4f} eV")
print(f"  LUMO 能量: {mo_energy[n_occ]*27.2114:.4f} eV")
print(f"  HOMO-LUMO 能隙: {(mo_energy[n_occ] - mo_energy[n_occ-1])*27.2114:.4f} eV")

# ============================================================
#  偶极矩
# ============================================================
dip = mf.dip_moment()
print(f"\n偶极矩 (Debye): {dip}")
