"""
示例：苯的 TDDFT 激发态计算

使用 Frank 生成的代码，可直接运行。
"""

# ============================================================
#  导入模块
# ============================================================
from pyscf import gto, dft

# ============================================================
#  基组设置
# ============================================================
BASIS = "6-31g*"

# ============================================================
#  分子定义
# ============================================================
# 苯 (C6H6)
mol = gto.Mole()
mol.atom = '''
    C  0.000  1.396  0.000
    C  1.209  0.698  0.000
    C  1.209 -0.698  0.000
    C  0.000 -1.396  0.000
    C -1.209 -0.698  0.000
    C -1.209  0.698  0.000
    H  0.000  2.479  0.000
    H  2.147  1.240  0.000
    H  2.147 -1.240  0.000
    H  0.000 -2.479  0.000
    H -2.147 -1.240  0.000
    H -2.147  1.240  0.000
'''
mol.basis = BASIS
mol.charge = 0
mol.spin = 0  # 自旋多重度 = 1 (单重态)
mol.verbose = 4
mol.build()

# ============================================================
#  DFT 基态计算
# ============================================================
mf = dft.RKS(mol)
mf.xc = 'B3LYP'
mf.kernel()

print(f"基态 DFT (B3LYP) 能量: {mf.e_tot:.10f} Hartree")

# ============================================================
#  TDDFT 激发态计算
# ============================================================
print("\n计算激发态...")
td = mf.TDDFT()
td.nstates = 6
td.kernel()
td.analyze()

# 打印激发能
print("\n激发态能量 (eV):")
for i, e in enumerate(td.e):
    print(f"  状态 {i+1}: {e*27.2114:.4f} eV ({1240.0/(e*27.2114):.1f} nm)")

# 打印振子强度
print("\n振子强度:")
for i, f in enumerate(td.oscillator_strength()):
    print(f"  状态 {i+1}: {f:.6f}")
