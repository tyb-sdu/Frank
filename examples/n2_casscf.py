"""
示例：氮气 CASSCF(6,6)/cc-pVDZ 计算

使用 Frank 生成的代码，可直接运行。
"""

# ============================================================
#  导入模块
# ============================================================
from pyscf import gto, scf, mcscf

# ============================================================
#  基组设置
# ============================================================
BASIS = "cc-pvdz"

# ============================================================
#  分子定义
# ============================================================
# 氮气 (N2)
mol = gto.Mole()
mol.atom = '''
    N  0.000  0.000  0.000
    N  0.000  0.000  1.098
'''
mol.basis = BASIS
mol.charge = 0
mol.spin = 0  # 自旋多重度 = 1 (单重态)
mol.verbose = 4
mol.build()

# ============================================================
#  HF 计算
# ============================================================
mf = scf.RHF(mol)
mf.kernel()

print(f"HF 能量: {mf.e_tot:.10f} Hartree")

# ============================================================
#  CASSCF 计算
# ============================================================
# CASSCF(6,6): 6 个活性轨道，6 个活性电子
# 适合 N2 的三键描述
mc = mcscf.CASSCF(mf, 6, 6)
mc.kernel()

print(f"CASSCF 能量: {mc.e_tot:.10f} Hartree")
print(f"\nCI 系数:")
print(mc.ci)

# 打印活性轨道
print(f"\n活性轨道数: {mc.ncas}")
print(f"活性电子数: {mc.nelecas}")
