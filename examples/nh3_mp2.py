"""
示例：氨分子 MP2/cc-pVDZ 计算

使用 Frank 生成的代码，可直接运行。
"""

# ============================================================
#  导入模块
# ============================================================
from pyscf import gto, scf, mp

# ============================================================
#  基组设置
# ============================================================
BASIS = "cc-pvdz"

# ============================================================
#  分子定义
# ============================================================
# 氨 (NH3)
mol = gto.Mole()
mol.atom = '''
    N  0.000  0.000  0.116
    H  0.000  0.939 -0.271
    H  0.813 -0.470 -0.271
    H -0.813 -0.470 -0.271
'''
mol.basis = BASIS
mol.charge = 0
mol.spin = 0  # 自旋多重度 = 1 (单重态)
mol.verbose = 4
mol.build()

# ============================================================
#  HF 计算
# ============================================================
mf = scf.RHF(mol)
mf.kernel()

print(f"HF 能量: {mf.e_tot:.10f} Hartree")

# ============================================================
#  MP2 计算
# ============================================================
pt = mp.MP2(mf)
pt.kernel()

print(f"MP2 相关能: {pt.e_corr:.10f} Hartree")
print(f"MP2 总能量: {pt.e_tot:.10f} Hartree")

# ============================================================
#  分子轨道分析
# ============================================================
mo_energy = mf.mo_energy
mo_occ = mf.mo_occ
n_occ = int(sum(mo_occ) / 2)

print(f"\n分子轨道分析:")
print(f"  占据轨道数: {n_occ}")
print(f"  HOMO 能量: {mo_energy[n_occ-1]*27.2114:.4f} eV")
print(f"  LUMO 能量: {mo_energy[n_occ]*27.2114:.4f} eV")
print(f"  HOMO-LUMO 能隙: {(mo_energy[n_occ] - mo_energy[n_occ-1])*27.2114:.4f} eV")
